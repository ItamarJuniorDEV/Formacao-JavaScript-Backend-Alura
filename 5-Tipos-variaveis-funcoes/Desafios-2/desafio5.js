/*
Crie uma variável com uma string e 
utilize métodos de manipulação de strings, como toUpperCase, toLowerCase,
slice ou outros, para modificar a string original. 
Exiba os resultados finais no console.
*/

let palavra = 'Mesa';

let palavraMaiuscula = palavra.toUpperCase();
console.log(palavraMaiuscula);

let palavraMinuscula = palavra.toLowerCase();
console.log(palavraMinuscula);

let palavraCortada = palavra.slice(2);
console.log(palavraCortada);